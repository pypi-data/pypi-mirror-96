class PostBaseModel(object):

    @classmethod
    def getBySpiderWebIdFloor(cls, spider, web_id, floor, session):
        return session.query(cls).filter(cls.spider == spider, cls.bbs_web_id == web_id, cls.bbs_floor == floor).all()


